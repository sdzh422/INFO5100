    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // TODO add your handling code here:
        String name = textName.getText();
        if(name.equals(""))
        {
              JOptionPane.showMessageDialog(null, "Please check your input.");  
              return;
        }
        if(system.checkIfNetworkIsUnique(name))
        {
            Network n = new Network();
            n.setName(name);
            system.getNetworkdirectory().getNetworklist().add(n);
             JOptionPane.showMessageDialog(null, "Created Successfully");  
             populateTable();
        }
        else
        {
             JOptionPane.showMessageDialog(null, "Please change another name.");  
        }
    }//GEN-LAST:event_btnAddActionPerformed

