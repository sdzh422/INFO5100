private void populateTable() {
        DefaultTableModel model = (DefaultTableModel) tblNetwork.getModel();
        model.setRowCount(0);
        int num = 1;
        for (Network temp : system.getNetworkdirectory().getNetworklist()) {
                   Object[] row = new Object[3];
                    row[0] = num;
                    row[1] = temp.getName();
                    row[2] = temp.getEnterpriseNum();
                    model.addRow(row);
                    num++;
           }            
        }
    

