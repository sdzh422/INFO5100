    public boolean checkInput(String username,String password,String name)
    {
            if(username==null||username.equals("")|| password==null||password.equals("") || name==null||name.equals("")   )          
         return false;
        if(!system.checkIfUserIsUnique(username))
            return false;
        return true;
    }
