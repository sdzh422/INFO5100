    private void btnSubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSubmitActionPerformed
        // TODO add your handling code here:
        String username = textUsername.getText();
        String password = textPassword.getText();
        String name = textName.getText();
        if(checkInput(username,password,name))
        {
             department.getUserAccountDirectory()
                     .createUserAccount(username, password, department.getDepartmentType(),new Employee(name), new ManagerRole());
             JOptionPane.showMessageDialog(null, "Create Successfully");   
             //back
        userProcessContainer.remove(this);
        Component[] componentArray = userProcessContainer.getComponents();
        Component component = componentArray[componentArray.length - 1];
        AdminWorkAreaJPanel Adminworkarea = (AdminWorkAreaJPanel) component;
        Adminworkarea.populateTree();
        CardLayout layout = (CardLayout) userProcessContainer.getLayout();
        layout.previous(userProcessContainer);
             
        }
       else
        {
            JOptionPane.showMessageDialog(null, "Please check your input");  
        }
    }//GEN-LAST:event_btnSubmitActionPerformed

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
        UserAccount ua = department.getUserAccountDirectory().getUserAccountList().get(0);
         String username = textUsername.getText();
        String password = textPassword.getText();
        String name = textName.getText();
        if(checkInput(username,password,name))
        {
            ua.getEmployee().setName(name);
            ua.setPassword(password);
            ua.setUsername(username);
            JOptionPane.showMessageDialog(null, "Create Successfully");  
        }
        else
        {
            JOptionPane.showMessageDialog(null, "Please check your input");  
        }
        
        
    }//GEN-LAST:event_btnUpdateActionPerformed

